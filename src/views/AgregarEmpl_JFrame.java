    private void combox_registrosActionPerformed(
            java.awt.event.ActionEvent evt) { // GEN-FIRST:event_combox_registrosActionPerformed
        String seleccionado = combox_registros.getSelectedItem().toString();
        switch (seleccionado) {
            case "Registro de areas" -> {
                GeneralMethods.openFrame(new AgregarArea_JFrame());
                this.dispose();
            }
            case "Registro de gerentes" -> {
                GeneralMethods.openFrame(new AgregarGeren_JFrame());
                this.dispose();
            }
            case "Registro de clientes" -> {
                GeneralMethods.openFrame(new AgregarCliente_JFrame());
                this.dispose();
            }
            case "Registro de productos" -> {
                GeneralMethods.openFrame(new AgregarPro_JFrame());
                this.dispose();
            }
            case "Registro de observaciones" -> {
                GeneralMethods.openFrame(new AgregarObs_JFrame());
                this.dispose();
            }
            case "Registro de ventas" -> {
                GeneralMethods.openFrame(new AgregarVenta_JFrame());
                this.dispose();
            }
            case "Registro de manufacturas" -> {
                GeneralMethods.openFrame(new AgregarManu_JFrame());
                this.dispose();
            }

        }
    } // GEN-LAST:event_combox_registrosActionPerformed

